using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ProLobbyCompanyProject.Model
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   The terabytes business company representative. </summary>
    ///
    /// <remarks>   Sasha Pavlovski, 1/12/2023. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class TBBusinessCompanyRepresentative
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier of the business company. </summary>
        ///
        /// <value> The identifier of the business company. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [Key]
        public int BusinessCompany_Id { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the name of the representative first. </summary>
        ///
        /// <value> The name of the representative first. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string RepresentativeFirstName { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the name of the representative last. </summary>
        ///
        /// <value> The name of the representative last. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string RepresentativeLastName { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the name of the company. </summary>
        ///
        /// <value> The name of the company. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string CompanyName { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets URL of the document. </summary>
        ///
        /// <value> The URL. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Url { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the email. </summary>
        ///
        /// <value> The email. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        [EmailAddress(ErrorMessage = "please enter right email")]
        public string Email { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the phone number. </summary>
        ///
        /// <value> The phone number. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Phone_number { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the identifier of the user. </summary>
        ///
        /// <value> The identifier of the user. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string User_Id { get; set; }


    }
}
